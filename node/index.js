const { commonConfig, customDisplayConfig } = configData
const customDisplayInfo = customDisplayConfig.find(item => item.sourceList.includes('kanjia'))

Object.keys(customDisplayInfo).forEach(key => {
    console.log('key', key)
    if (key === 'sourceList') {
        // key为sourceList不处理
        return
    }
    const val = customDisplayInfo[key]
    if (typeof val === 'string' || typeof val === 'number' || typeof val === 'boolean') {
        commonConfig[key] = val
    } else if (Object.prototype.toString.call(val) === '[object Object]') {
        commonConfig[key] = Object.assign({}, commonConfig[key], val)
    } else if (key === 'couponDetail') {
        val.forEach((productionLineCouponDetail) => {
            const { bizType } = productionLineCouponDetail
            let index = commonConfig.couponDetail.findIndex(ele => ele.bizType === bizType)
            commonConfig.couponDetail[index] = Object.assign({}, commonConfig.couponDetail[index], productionLineCouponDetail)
        })
    }
})
